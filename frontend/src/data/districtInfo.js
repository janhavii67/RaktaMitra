/**
 * Get district info by name (case-insensitive)
 */
export function getDistrictInfo(name) {
  const key = name.toLowerCase().replace(/\s+/g, '');

  if (districtData[key]) return districtData[key];

  for (const [k, v] of Object.entries(districtData)) {
    if (k.includes(key) || key.includes(k) || v.name.toLowerCase() === name.toLowerCase()) {
      return v;
    }
  }

  return {
    name: name,
    division: 'Maharashtra',
    headquarters: name,
    area: 'N/A',
    population: 'N/A',
    established: 'N/A',
    highlights: ['Part of Maharashtra'],
    color: '#818cf8',
  };
}

